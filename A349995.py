from labmath import *       # Available via pip (https://pypi.org/project/labmath/)

def oddsqfrsemi():
    # Generates the odd squarefree semiprimes by filtering the squares and evens out of the semiprimes.
    pg = primegen()
    semis = semiprimegen()
    p = next(pg)
    pp = p*p
    while True:
        semi = next(semis)
        if pp == semi:
            p = next(pg)
            pp = p*p
            continue
        if semi % 2 == 1: yield semi

